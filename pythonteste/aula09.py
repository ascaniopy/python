frase = 'Curso em Vídeo Python'
print(frase)                               # Curso em vídeo Python
print(frase[3])                            # s
print(frase[:13])                          # Curso em vide
print(frase[13:])                          # o Python
print(frase[1:15:2])                       # us mVdo
print(frase.count('o'))                    # 3(Conta 3 os).
print(len(frase))                          # 21(Tamanho da frase).
print(frase.replace('Python', 'Android'))  # Troca Python por Android.
print('Curso' in frase)                    # True
print(frase.find('Vídeo'))                 # 9 (Vídeo está na posição 9).
print(frase.lower().find('vídeo'))         # 9
print(frase.split())                       # ['Curso', 'em', 'Vídeo', 'Python']





